		<p align="center"><font color="#0000B2"><h1>Welcome to Eat2Gather!</h1></font></p>

		<br>

		<p>In KAIST, eating seems like a lonely chore that we must get out of the way in order to go to class. However, we believe that eating should be an enjoyable and social part of the day where students are able to relax and get away from all the stress of their academic work, at least momentarily, through interaction and conversation. Speaking from our personal experiences, in high school (before KAIST) lunch and dinner time was always family time. It was the time when we were able to interact with our family and discuss how our days were going. We want to bring this spirit back into lunch and dinner time at KAIST.</p>

		<br>

		<p>In addition, we feel that there are not enough opportunities for students to meet other students and make new friends, and that there is an awkward and uncomfortable atmosphere when meeting new people. The results of the survey provide evidence for our claims, and we want to change such an atmosphere.</p>

		<br>

		<p>Both the lack of a social aspect when eating lunch/dinner and the presence of an uncomfortable atmosphere when meeting new people is what serves as our motivation for Eat2Gather. People need an “excuse” for an approach to unknown people. We would like to provide that platform on which people can interact, making it much more easier and comfortable for students to contact each other. By doing so, we hope to address both problems and allow for easier initiations of social interactions between students in KAIST. </p>
